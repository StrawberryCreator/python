import turtle as t

t.bgcolor ("black")
t.speed (0)

flag = input("What flag do you want to see (1 = Netherlands, 2 = Guatemala)? ")

if flag == "1":
    t.penup ()
    t.color ("#c81021")
    t.goto (-150, 99)
    t.begin_fill ()
    t.goto (150, 99)
    t.goto (150, 33)
    t.goto (-150, 33)
    t.goto (-150, 99)
    t.end_fill ()
    t.penup ()
    t.color ("white")
    t.goto (-150, 33)
    t.begin_fill ()
    t.goto (150, 33)
    t.goto (150, -33)
    t.goto (-150, -33)
    t.goto (-150, 33)
    t.end_fill ()
    t.penup ()
    t.color ("#003da5")
    t.goto (-150, -33)
    t.begin_fill ()
    t.goto (150, -33)
    t.goto (150, -99)
    t.goto (-150, -99)
    t.goto (-150, -33)
    t.end_fill ()
else:
    t.penup ()
    t.color ("#4998d0")
    t.goto (-150, 100)
    t.begin_fill ()
    t.goto (-50, 100)
    t.goto (-50, -100)
    t.goto (-150, -100)
    t.goto (-150, 100)
    t.end_fill ()
    t.penup ()
    t.color ("white")
    t.goto (-50, 100)
    t.begin_fill ()
    t.goto (50, 100)
    t.goto (50, -100)
    t.goto (-50, -100)
    t.goto (-50, 100)
    t.end_fill ()
    t.penup ()
    t.color ("#4998d0")
    t.goto (150, 100)
    t.begin_fill ()
    t.goto (50, 100)
    t.goto (50, -100)
    t.goto (150, -100)
    t.goto (150, 100)
    t.end_fill ()

t.mainloop ()